CREATE TABLE IF NOT EXISTS general_fund_payment (
    id BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    PAYMENT_ID VARCHAR(36) NOT NULL,
    PAYMENTDETAIL_ID VARCHAR(36) NOT NULL,
    PAYMENTDATE DATETIME NULL,
    VALUEDATE DATETIME NULL,
    PAIDBY VARCHAR(150) NULL,
    RECEIPTNO VARCHAR(30) NULL,
    PAYMENT_AMOUNT DECIMAL(15,2) NULL,
    PAYMODE_CT VARCHAR(10) NULL,
    AFTYPE VARCHAR(20) NULL,
    PAYGROUP_CT VARCHAR(10) NULL,
    LOCAL_TIN VARCHAR(50) NULL,
    USERID VARCHAR(50) NULL,
    COLLECTOR VARCHAR(50) NULL,
    PAYMENT_STATUS_CT VARCHAR(20) NULL,
    VOID_BV TINYINT(1) NULL,
    PROV_BV TINYINT(1) NULL,
    AMOUNTPAID DECIMAL(15,2) NULL,
    PAYMENTDETAIL_ITAXTYPE_CT VARCHAR(10) NULL,
    CASETYPE_CT VARCHAR(10) NULL,
    PAYMENTDETAIL_STATUS_CT VARCHAR(20) NULL,
    SOURCEID VARCHAR(50) NULL,
    SOURCE_CT VARCHAR(50) NULL,
    FUNDTYPE_CT VARCHAR(10) NULL,
    RECEIPTITEMORDER INT NULL,
    UNIT VARCHAR(50) NULL,
    RATE_ITAXTYPE_CT VARCHAR(10) NULL,
    OPGROUP VARCHAR(50) NULL,
    OPSUBGROUP VARCHAR(50) NULL,
    RATE_DESCRIPTION VARCHAR(255) NULL,
    RATE DECIMAL(15,4) NULL,
    TAX_CODE VARCHAR(10) NULL,
    TAX_DESCRIPTION VARCHAR(255) NULL,
    TAX_GROUP VARCHAR(20) NULL,
    TAX_SUBGROUP VARCHAR(20) NULL,
    TAX_MAIN_GROUP VARCHAR(20) NULL,
    TAX_FUNDTYPE_CT VARCHAR(10) NULL,
    KEY idx_gfp_paymentdate (PAYMENTDATE),
    KEY idx_gfp_receiptno (RECEIPTNO),
    KEY idx_gfp_paymentid (PAYMENT_ID),
    KEY idx_gfp_paymentdetailid (PAYMENTDETAIL_ID),
    KEY idx_gfp_sourceid (SOURCEID),
    KEY idx_gfp_fundtype (FUNDTYPE_CT)
);
