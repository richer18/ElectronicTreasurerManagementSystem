CREATE TABLE IF NOT EXISTS community_tax_certificate_payment (
    id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    CTC_ID INT NOT NULL,
    DATEISSUED DATE NULL,
    CTCNO VARCHAR(50) NULL,
    LOCAL_TIN VARCHAR(50) NULL,
    OWNERNAME VARCHAR(255) NULL,
    BASICTAXDUE DECIMAL(15,2) DEFAULT 0.00,
    BUSTAXAMOUNT DECIMAL(15,2) DEFAULT 0.00,
    BUSTAXDUE DECIMAL(15,2) DEFAULT 0.00,
    SALTAXAMOUNT DECIMAL(15,2) DEFAULT 0.00,
    SALTAXDUE DECIMAL(15,2) DEFAULT 0.00,
    RPTAXAMOUNT DECIMAL(15,2) DEFAULT 0.00,
    RPTAXDUE DECIMAL(15,2) DEFAULT 0.00,
    INTEREST DECIMAL(15,2) DEFAULT 0.00,
    TOTALAMOUNTPAID DECIMAL(15,2) DEFAULT 0.00,
    TRANSDATE DATETIME NULL,
    USERID VARCHAR(50) NULL,
    CTCTYPE VARCHAR(20) NULL,
    CTCYEAR YEAR NULL,
    INUSE_BV TINYINT(1) DEFAULT 1,
    MUNICIPAL_ID INT NULL,
    PAYMENT_STATUS_CT VARCHAR(20) NULL,
    IS_CANCELLED TINYINT(1) DEFAULT 0
);
