CREATE TABLE IF NOT EXISTS real_property_tax_payment (
    ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    DATE DATE NULL,
    PAID_BY VARCHAR(150) NULL,
    NAME_OF_TAXPAYER VARCHAR(1000) NULL,
    PERIOD_COVERED VARCHAR(20) NULL,
    PIN VARCHAR(100) NULL,
    OR_NO VARCHAR(30) NULL,
    NAME_OF_BARANGAY VARCHAR(100) NULL,
    BASIC_CURRENT_YEAR DECIMAL(18,4) NULL,
    BASIC_CURRENT_PENALTIES DECIMAL(18,4) NULL,
    BASIC_DISCOUNTS DECIMAL(18,4) NULL,
    BASIC_IMMEDIATE DECIMAL(18,4) NULL,
    BASIC_PRECEDING_YEAR DECIMAL(18,4) NULL,
    BASIC_PRECEDING_PENALTIES DECIMAL(18,4) NULL,
    BASIC_PRIOR_YEARS DECIMAL(18,4) NULL,
    BASIC_PRIOR_PENALTIES DECIMAL(18,4) NULL,
    BASIC_TOTAL DECIMAL(18,4) NULL,
    BASIC_25_SHARE DECIMAL(18,4) NULL,
    SEF_CURRENT_YEAR DECIMAL(18,4) NULL,
    SEF_CURRENT_PENALTIES DECIMAL(18,4) NULL,
    SEF_DISCOUNTS DECIMAL(18,4) NULL,
    SEF_IMMEDIATE DECIMAL(18,4) NULL,
    SEF_PRECEDING_YEAR DECIMAL(18,4) NULL,
    SEF_PRECEDING_PENALTIES DECIMAL(18,4) NULL,
    SEF_PRIOR_YEARS DECIMAL(18,4) NULL,
    SEF_PRIOR_PENALTIES DECIMAL(18,4) NULL,
    SEF_TOTAL DECIMAL(18,4) NULL,
    BASIC_AND_SEF DECIMAL(18,4) NULL,
    PROPERTY_CLASSIFICATION VARCHAR(255) NULL,
    CASHIER VARCHAR(50) NULL,
    PAYMENT_STATUS_CT VARCHAR(20) NULL,
    IS_CANCELLED TINYINT(1) DEFAULT 0
);
